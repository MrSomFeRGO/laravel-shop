                    организацией.</p>

                <p style="text-align: justify; text-indent: 36pt;"> Ежегодно рабочие, служащие и администрация
                    предприятия совместно с профсоюзной организацией РУПП «Гранит», ОО «БРСМ», РОО «Белая Русь» в канун
                    новогодних праздников проводят благотворительные акции «Подари детям тепло» для детей-сирот
                    Социально-педагогического центра с детским приютом в г. Лунинце и для детей Микашевичского филиала
