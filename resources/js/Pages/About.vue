                <p style="text-align: justify; text-indent: 36pt;">Предприятием ежегодно проводятся реконструкции
                    устаревших конструкций, автомобильных дорог и капитальные ремонты помещений, зданий цехов,
                    корпусов.</p>

                <p style="text-align: justify; text-indent: 36pt;"></p>

                <p style="text-align: justify; text-indent: 36pt;"><b> Техпроцесс </b></p>

                <p style="text-align: justify; text-indent: 36pt;">РУПП «Гранит» - многофункциональный технологический
                    комплекс.</p>

                <p style="text-align: justify; text-indent: 36pt;">Технологический процесс производства щебня и отсева
                    сосредоточен в трёх основных структурных подразделениях:</p>

                <p style="text-align: justify; text-indent: 36pt;">• карьер «Микашевичи»;</p>

                <p style="text-align: justify; text-indent: 36pt;">• автотранспортный парк;</p>

                <p style="text-align: justify; text-indent: 36pt;">• дробильно-сортировочный завод.</p>

                <p style="text-align: justify; text-indent: 36pt;"></p>

                <p style="margin: 0 0 0.0001pt; text-align: justify; text-indent: 36pt;"><b>Карьер «Микашевичи».</b>
                </p>

                <p style="margin: 0 0 0.0001pt; text-align: justify; text-indent: 36pt;">Технологическая схема
                    разработки горных пород в карьере «Микашевичи» предусматривает ведение вскрышных, буровзрывных и
                    добычных работ. </p>

                <p style="text-align: justify; text-indent: 36pt;">Процесс добычи полезных ископаемых в карьере
                    «Микашевичи» состоит из трех этапов: вскрытие полезных ископаемых (цех Горный), подготовка горной
                    породы к выемке (Цех Буровзрывных работ), погрузка взорванной горной массы в автотранспорт, откачка
                    притока грунтовых вод (Цех Горный). </p>

                <p style="margin: 0 0 0.0001pt; text-align: justify; text-indent: 36pt;">Цех Горный занимается
                    удалением вскрышных пород (растительный слой, песчаная вскрыша, скальная вскрыша) для вскрытия
                    полезного ископаемого. Вскрышные породы отгружаются в автотранспорт для вывозки во внешние отвалы.
                    Мощность удаляемых вскрышных пород составляет от 8 до 60 метров. После окончания отсыпки отвалов
                    производят выполаживание откосов и отсыпку площадей растительным слоем, снятым перед вскрышными
                    работами, с целью выполнения рекультивации. </p>

                <p style="margin: 0 0 0.0001pt; text-align: justify; text-indent: 36pt;">Применяемая техника:
                    экскаваторы ЭШ-11/70, ЭКГ-5А, ЭДГ-3.2-30, бульдозеры-Т-330, Т-25, БелАЗ-7822, погрузчик
                    БелАЗ-7823.</p>

                <p style="text-align: justify; text-indent: 36pt;">Цех Буровзрывных работ производит бурение скважин
                    диаметром 250 мм, 244,5мм, 215мм, бурение шпуров Ø 40мм в негабаритных кусках, их последующую
                    зарядку взрывчатыми веществами и взрывание монолита и негабаритных кусков. Конечная цель его работы
                    состоит в подготовке к экскавации достаточного для переработки количества горной массы. За год
                    производится более 100 массовых взрывов. Применяются бурстанки СБШ - 250МНА, МНА-32, автотракторная
                    и бульдозерная техника, а также автомобили для зарядки и забойки скважин.</p>

                <p style="text-align: justify; text-indent: 36pt;"> Цех Горный производит добычные работы, состоящие из
                    погрузки горной массы из забоев в автотранспорт для транспортировки на дробильно-сортировочный
                    завод, устройство карьерных дорог, съездов, зумпферов, кюветов, обеспечивает работы водоотливных
                    установок. Откачка суточного притока грунтовых вод и атмосферных осадков составляет 65 тыс.
                    м<sup>3</sup>.Для откачки воды с нижних горизонтов используются насосы АЦН -1000/180.
                    Производительность по отгрузке взорванной массы составляет более 60 тыс. тонн в сутки. В цехе
                    работает мощная горнотранспортная техника: экскаваторы ЭКГ-8И, ЭКГ-10, ЭКГ-5А, бульдозеры Т-330,
                    погрузчик БелАЗ-7822, бульдозер БелАЗ-7823. Для разрушения негабаритных кусков горной массы
                    используется гидравлический экскаватор «HYUNDAI ROBER 450 LC-7» с навесным оборудованием гидромолот,
                    который разрушает негабаритные куски энергией механического удара.</p>

                <p style="text-align: justify; text-indent: 36pt;"></p>

                <p style="text-align: justify; text-indent: 36pt;"><b>Автотранспортный парк.</b></p>

                <p style="text-align: justify; text-indent: 36pt;">Автотранспортный парк представляет собой одно из
                    сложнейших подразделений РУПП «Гранит». Он является связующим звеном между карьером и
                    дробильно-сортировочным заводом.</p>

                <p style="text-align: justify; text-indent: 36pt;">На самом сложном и интенсивном участке трудится
                    первая транспортная колонна. В её распоряжении 61 автомобиль БелАЗ грузоподъемностью 50, 55, 60, 90
                    тонн.</p>

                <p style="text-align: justify; text-indent: 36pt;">Водители 2 колонны задействованы в
                    материально-техническом снабжении предприятия. Они обеспечивают грузовые и пассажирские перевозки по
                    местным, междугородним и международным маршрутам. </p>

                <p style="text-align: justify; text-indent: 36pt;">За 2-й колонной закреплены автокраны, легковой
                    транспорт, автобусы, автовышки, автомобили по перевозке ГСМ и взрывчатых веществ, МАЗы для
                    междугородных перевозок.</p>

                <p style="text-align: justify; text-indent: 36pt;">Техника третьей колонны, где сосредоточены колесные и
                    гусеничные бульдозеры, погрузчики на базе БелАЗов и тракторы, обеспечивает бесперебойную и
                    непрерывную работу предприятия по выпуску щебня, погрузке продукции, содержанию дорог и выполнение
                    прочих работ.</p>

                <p style="text-align: justify; text-indent: 36pt;"></p>

                <p style="text-align: justify; text-indent: 36pt;"><b> Дробильно-сортировочный завод.</b></p>

                <p style="text-align: justify; text-indent: 36pt;">В состав дробильно-сортировочного завода входят пять
                    цехов по переработке горной массы и производству щебня, один цех погрузки и два вспомогательных
                    участка.</p>

                <p style="text-align: justify; text-indent: 36pt;"> Привезенную из карьера горную массу размером
                    0-1200мм цеха перерабатывают на щековых дробилках С-200(Финляндия), СМД-117, ЩДП 15х21(Россия).</p>

                <p style="text-align: justify; text-indent: 36pt;">Горная масса 0-300 мм конвейерным транспортом
                    подается на дробилки среднего дробления КСД-2200(Россия), НР-500(Франция), дробится на размер горной
                    массы от 0 до 90 мм и подается конвейерным транспортом в корпус третичного дробления.</p>

                <p style="text-align: justify; text-indent: 36pt;">Принятая горная масса перерабатывается на конусных
                    дробилках НР-300, НР-500 (Франция), КМД-2200(Россия) размером от 0 до 70 мм и подается в корпус
                    сортировки, где проходит грохочение на различные фракции.</p>

                <p style="text-align: justify; text-indent: 36pt;"> Складирование готовой продукции осуществляется в
                    штабельные склады.</p>

                <p style="text-align: justify; text-indent: 36pt;"> Отгрузка готовой продукции производится в
                    железнодорожный, автомобильный и водный транспорт.</p>

                <p style="text-align: justify; text-indent: 36pt;">Весь технологический процесс переработки горной массы
                    на заводе автоматизирован. Управление и контроль за ходом процесса осуществляется с центрального
                    пульта дробильно-сортировочного завода.</p>

                <p style="text-align: justify; text-indent: 36pt;">Выпуск щебня различных фракций сориентирован на
                    обеспечение строительной индустрии Республики Беларусь строительным материалом - щебнем из плотных
                    горных пород. Структуры щебня устойчивы против всех видов распадов. Прочность щебня по дробимости
                    характеризуется маркой 1400, по истираемости - И1, морозостойкость - 300.</p>

                <p style="text-align: justify; text-indent: 36pt;"></p>

                <p style="text-align: justify; text-indent: 36pt;"><b>ЛМЦ</b></p>

                <p style="text-align: justify; text-indent: 36pt;"> В 1983 году был построен литейный цех, который
                    является основным поставщиком запасных частей из стали 110Г13Л и 35Л для размольно-дробильного и
                    горнодобывающего оборудования. Производственная мощность ЛМЦ - <strong>1000 тонн литья в
                        год</strong>.</p>

                <p style="text-align: justify; text-indent: 36pt;">Собственный модельный участок позволяет в минимальные
                    сроки изготавливать оснастку для выпуска несложных отливок по индивидуальным чертежам заказчика. Для
                    выплавки стали установлены две электроплавильные печи ДСП-3.</p>

                <p style="text-align: justify; text-indent: 36pt;">На оборудовании ЛМЦ имеется возможность производить
                    механическую обработку литья.</p>

                <p style="text-align: justify; text-indent: 36pt;"></p>

                <p style="text-align: justify; text-indent: 36pt;">Не последнюю роль в технологическом процессе играют и
                    вспомогательные цеха предприятия: КИПиА, ремонтно-механический, электроцех, цех паросилового
                    хозяйства, ремонтно-строительный и другие.</p>

                <p style="text-align: justify; text-indent: 36pt;"> Для поддержания нравственного и физического здоровья
                    многотысячного коллектива РУПП «Гранит» на предприятии десятилетиями создавалась и обслуживалась
                    большая социальная инфраструктура, включающая Дворец культуры, бассейн, санаторий «Свитанок», ФОК,
                    стадион, два общежития. В декабре 2015 года был создан музей РУПП «Гранит».</p>

                <p style="text-align: justify; text-indent: 36pt;">За последние годы часть этой социальной сферы была
                    передана на баланс Микашевичского ЖКХ.</p>

                <p style="text-align: justify; text-indent: 36pt;">В 2017 году была открыта новая просторная столовая
                    предприятия с новейшим оборудованием. Налажена работа еще двух небольших цеховых столовых,
                    буфета.</p>

                <p style="text-align: justify; text-indent: 36pt;">Для активного досуга рабочих, служащих и их детей за
                    последние годы предприятием было сделано многое:</p>

                <p style="text-align: justify; text-indent: 36pt;">- открыт уличный физкультурно-спортивный комплекс с
                    зоной активного отдыха на территории ДК;</p>

                <p style="text-align: justify; text-indent: 36pt;">-открыта детская игровая площадка с электромобилями,
                    батутами, надувными горками;</p>

                <p style="text-align: justify; text-indent: 36pt;">-открыт детский игровой комплекс «Муравейник»;</p>

                <p style="text-align: justify; text-indent: 36pt;">- в зимнее время заливается каток и налажена работа
                    по прокату зимнего спортивного инвентаря.</p>

                <p style="text-align: justify; text-indent: 36pt;">Во Дворце культуры РУПП «Гранит» налажена и
                    проводится на высоком уровне круглогодичная театрализованная программа для зрителей всех
                    возрастов.</p>

                <p style="text-align: justify; text-indent: 36pt;">Ежегодно проводятся разные многоборья по различным
                    видам спорта, туристический слет РУПП «Гранит».</p>

                <p style="text-align: justify; text-indent: 36pt;">Так же, во Дворце культуры, успешно работают
                    различные коллективы: инструментальные вокально-хоровые, театрально-драматические, хореографические.
                    А также 5 детских и 3 взрослых коллектива художественной самодеятельности.</p>

                <p style="text-align: justify; text-indent: 36pt;"> РУПП «Гранит» ежегодно проводит молодежный
                    музыкальный фестиваль «Мы едины – мы молодежь».</p>

                <p style="text-align: justify; text-indent: 36pt;"> Предприятие выпускает свою малотиражную газету
                    «Гранит-обозрение». Она ежемесячно информирует коллектив о деятельности администрации, рассказывает
                    рабочим и служащим о буднях родного предприятия, его лучших людях, знакомит со спецификой работы
